  let currentLocale = "pt";

  function detectBrowserLocale() {
    const langs = navigator.languages && navigator.languages.length ? navigator.languages : [navigator.language || "en"];
    for (const raw of langs) {
      if (String(raw || "").toLowerCase().startsWith("pt")) return "pt";
    }
    return "en";
  }

  function normalizeLocale(loc) {
    return loc === "en" ? "en" : loc === "pt" ? "pt" : null;
  }

  function formatTemplate(str, params) {
    if (!params) return str;
    return String(str).replace(/\{(\w+)\}/g, (_, k) =>
      params[k] != null ? String(params[k]) : `{${k}}`,
    );
  }

  function t(key, params) {
    const bag = MS[currentLocale] || MS.pt;
    const fallback = MS.pt[key] ?? MS.en[key] ?? key;
    const raw = bag[key] ?? fallback;
    return formatTemplate(raw, params);
  }

  function applyI18n(root) {
    const scope = root && root.querySelectorAll ? root : document;
    scope.querySelectorAll("[data-i18n]").forEach((el) => {
      const k = el.getAttribute("data-i18n");
      if (!k) return;
      if (el.hasAttribute("data-i18n-html")) el.innerHTML = t(k);
      else el.textContent = t(k);
    });
    scope.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
    });
    scope.querySelectorAll("[data-i18n-title]").forEach((el) => {
      const v = t(el.getAttribute("data-i18n-title"));
      el.title = v;
      if (el.hasAttribute("data-i18n-aria")) el.setAttribute("aria-label", v);
    });
    scope.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      if (!el.hasAttribute("data-i18n-title")) {
        el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria")));
      }
    });
    scope.querySelectorAll("option[data-i18n]").forEach((el) => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    document.title = `${document.querySelector(".brand")?.textContent?.trim() || "DashFlex"} ${t("branding.title_suffix")}`;
  }

  function setLocale(loc, opts) {
    const o = opts || {};
    const next = normalizeLocale(loc) || detectBrowserLocale();
    currentLocale = next;
    document.documentElement.lang = next === "pt" ? "pt-BR" : "en-US";
    document.documentElement.dataset.locale = next;
    if (o.persistLocal !== false) {
      try {
        localStorage.setItem(LOCALE_STORAGE_KEY, next);
      } catch (_) {}
    }
    applyI18n(document);
    window.dispatchEvent(new CustomEvent("dashflex:locale", { detail: next }));
    return next;
  }

  function getLocale() {
    return currentLocale;
  }

  function initLocale(preferred) {
    const fromPreferred = normalizeLocale(preferred);
    if (fromPreferred) return setLocale(fromPreferred, { persistLocal: true });
    let stored = null;
    try {
      stored = localStorage.getItem(LOCALE_STORAGE_KEY);
    } catch (_) {}
    const fromStorage = normalizeLocale(stored);
    if (fromStorage) return setLocale(fromStorage, { persistLocal: false });
    return setLocale(detectBrowserLocale(), { persistLocal: false });
  }

  window.DashFlexI18n = {
    t,
    applyI18n,
    setLocale,
    getLocale,
    initLocale,
    detectBrowserLocale,
    LOCALE_STORAGE_KEY,
    LOCALES,
  };
